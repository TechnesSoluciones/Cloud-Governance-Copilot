// Legacy types and interface for backward compatibility
export interface SelectOption {
  value: string;
  label: string;
  disabled?: boolean;
}

/**
 * Convenience wrapper component for simple select use cases
 * Provides a simplified API with options array instead of modular subcomponents
 *
 * Usage example:
 * ```tsx
 * <SimpleSelect
 *   value={selected}
 *   onValueChange={setSelected}
 *   options={[
 *     { value: 'option1', label: 'Option 1' },
 *     { value: 'option2', label: 'Option 2' },
 *   ]}
 *   placeholder="Select an option"
 * />
 * ```
 */
export interface SimpleSelectProps {
  value?: string;
  onValueChange?: (value: string) => void;
  placeholder?: string;
  options: SelectOption[];
  disabled?: boolean;
  className?: string;
  label?: string;
  error?: string;
}

export const SimpleSelect: React.FC<SimpleSelectProps> = ({
  value,
  onValueChange,
  placeholder = 'Select an option',
  options,
  disabled = false,
  className = '',
  label,
  error,
}) => {
  return (
    <div className={`space-y-2 ${className}`}>
      {label && (
        <label className="text-sm font-medium text-foreground">
          {label}
        </label>
      )}
      <Select value={value} onValueChange={onValueChange} disabled={disabled}>
        <SelectTrigger
          className={error ? 'border-red-500' : ''}
          aria-label={label || placeholder}
          aria-invalid={!!error}
          aria-describedby={error ? `${label}-error` : undefined}
        >
          <SelectValue placeholder={placeholder} />
        </SelectTrigger>
        <SelectContent>
          {options.map((option) => (
            <SelectItem key={option.value} value={option.value} disabled={option.disabled}>
              {option.label}
            </SelectItem>
          ))}
        </SelectContent>
      </Select>
      {error && (
        <p id={`${label}-error`} className="text-sm text-red-500" role="alert">
          {error}
        </p>
      )}
    </div>
  );
};
